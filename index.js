// Variable declaration using var, let, and const
var name = "John";  // Mutable variable using var
let age = 25;      // Mutable variable using let
const PI = 3.1415; // Immutable variable using const

// Modifying variable values
name = "Jane";
age = 30;

console.log(name); // Output: Jane
console.log(age);  // Output: 30
console.log(PI);   // Output: 3.1415

var fruit = "apple";
let quantity = 5;

console.log(fruit);     // Output: apple
console.log(quantity);  // Output: 5

quantity *= 2;

console.log(quantity);  // Output: 10
